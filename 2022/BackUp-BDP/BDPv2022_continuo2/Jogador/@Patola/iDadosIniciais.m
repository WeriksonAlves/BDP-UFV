function iDadosIniciais(rBDP)

% Dados iniciais dos Jogadores

% Cor do time
% c: Cyan ou y: Yellow
rBDP.pTime   = 'y';

%  Lado de ataque
rBDP.pLado   = 1;

% Atribuição inicial das cores das camisas
% r: Red
% g: Green
% b: Blue
% m: Magenta
rBDP.pCor    = 'r';

% Função em campo
% g: goleiro
% d: defensor
% a: atacante
rBDP.pFuncao = 'g';